#include <stdio.h>

int main() {
    // Integer type
    int age = 18;

    // Float type
    float height = 5.9;

    // Double type
    double pi = 3.1415926535;

    // Character type
    char grade = 'A';

    // Output
    printf("Integer (age): %d\n", age);
    printf("Float (height): %.2f\n", height);
    printf("Double (pi): %.10lf\n", pi);
    printf("Character (grade): %c\n", grade);

    return 0;
}
